var animals = ["Cat" , "Dog" , "Cow" , "Fish" ]
alert(animals)

//.push-добавляет элемент в КОНЕЦ массива
animals.push("horse")
alert(animals)

//.unshif-добавляет элемент в НАЧАЛО массива
animals.unshiftt("Goose")
alert(animals)

//.pop-удаляет  ПОСЛЕДНИЙ элемент в массиве
animals.pop()
alert(animals)

//.shift - удаляет ПЕРВЫЙ элемент в массиве
animals.shift()
alert(animals)